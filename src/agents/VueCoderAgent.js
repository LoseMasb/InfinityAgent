function escapeHtml(input) {
  return String(input || "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function normalizeFiles(result) {
  const files = result?.files;
  if (!files || typeof files !== "object" || Array.isArray(files)) {
    throw new Error("VueCoderAgent response must include a files object.");
  }

  return {
    files: Object.fromEntries(
      Object.entries(files).map(([filePath, content]) => [filePath, String(content)])
    ),
    implementationNotes: Array.isArray(result.implementationNotes)
      ? result.implementationNotes
      : []
  };
}

