export class VueCoderAgent extends BaseAgent {
  async run(input, context) {
    this.emit(context, "生成 Vite + Vue 3 项目文件");

    if (context.config.offline) {
      return {
        files: offlineFiles({
          name: input.name,
          brief: input.brief,
          content: input.content
        }),
        implementationNotes: ["Generated by deterministic offline VueCoderAgent."]
      };
    }

    const skills = this.skillRegistry.promptBlock(
      this.selectedSkills(context, [
        "vue-website-maker",
        "design-system",
        "vue-data-driven-pages"
      ])
    );

    const result = await this.llm.chatJson({
      agentName: this.name,
      system: `You are VueCoderAgent. Generate a complete Vite + Vue 3 website project.
Return only JSON with this exact shape:
{
  "files": {
    "package.json": "file contents",
    "index.html": "file contents",
    "vite.config.js": "file contents",
    "src/main.js": "file contents",
    "src/data/site.js": "file contents",
    "src/App.vue": "file contents",
    "src/styles.css": "file contents",
    "README.md": "file contents"
  },
  "implementationNotes": ["short notes"]
}

Rules:
- Use Vue 3 single-file components and Vite.
- Do not use markdown fences around file contents.
- All file paths must be relative and must not include "..".
- The generated site must be usable as the first screen, not a marketing placeholder.
- Keep UI responsive and avoid overlapping text.
- Prefer a data-driven App.vue that imports content from src/data/site.js.
- Let src/data/site.js export title, seo, nav, hero, metrics, sections, and closing content when useful.
- If you add dependencies, include them in package.json.`,
      user: JSON.stringify(
        {
          brief: input.brief,
          projectName: input.name,
          plan: input.plan,
          design: input.design,
          content: input.content,
          skills
        },
        null,
        2
      )
    });

    return normalizeFiles(result);
  }

  async revise(input, context) {
    this.emit(context, "根据审查意见进行一次代码修订");

    if (context.config.offline) {
      return input.currentCode;
    }

    const result = await this.llm.chatJson({
      agentName: this.name,
      system: `You are VueCoderAgent. Revise the full Vue project files based on review feedback.
Return only JSON with this exact shape:
{
  "files": {},
  "implementationNotes": ["short notes"]
}`,
      user: JSON.stringify(
        {
          brief: input.brief,
          plan: input.plan,
          design: input.design,
          content: input.content,
          currentFiles: input.currentCode.files,
          review: input.review
        },
        null,
        2
      )
    });

    return normalizeFiles(result);
  }
}
