export class ContentAgent extends BaseAgent {
  async run(input, context) {
    this.emit(context, "构建内容策略、首屏文案和 SEO 结构");

    if (context.config.offline) {
      return offlineContent(input);
    }

    const skills = this.skillRegistry.promptBlock(
      this.selectedSkills(context, ["content-strategy", "vue-data-driven-pages"])
    );

    return this.llm.chatJson({
      agentName: this.name,
      system: `You are ContentAgent. Turn a website brief into structured content for a Vue site.
Return only JSON with this shape:
{
  "voice": "brand voice",
  "seo": {
    "title": "page title",
    "description": "meta description",
    "keywords": ["keyword"]
  },
  "hero": {
    "eyebrow": "small label",
    "headline": "hero headline",
    "lead": "supporting copy",
    "primaryCta": "primary CTA",
    "secondaryCta": "secondary CTA"
  },
  "metrics": [{ "value": "metric", "label": "caption" }],
  "sections": [
    {
      "id": "work",
      "eyebrow": "section label",
      "heading": "section heading",
      "lead": "section lead",
      "items": [{ "tag": "01", "title": "item title", "description": "item description" }],
      "steps": [{ "title": "step title", "text": "step text" }]
    }
  ],
  "closing": {
    "eyebrow": "label",
    "heading": "closing heading",
    "copy": "closing copy",
    "email": "contact email",
    "cta": "cta text"
  }
}`,
      user: JSON.stringify(
        {
          brief: input.brief,
          projectName: input.name,
          plan: input.plan,
          design: input.design,
          skills
        },
        null,
        2
      )
    });
  }
}
