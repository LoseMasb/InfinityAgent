import { BaseAgent } from "./BaseAgent.js";
import { titleFromName } from "../utils/slug.js";

function briefSummary(brief) {
  return String(brief || "")
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .slice(0, 3)
    .join(" ");
}

